class Switch
{
public static void main(String args[])
{

int num = 15;

switch(num)
{

case 1 : 
System.out.println("num value is 1");
break;

case 2 :
System.out.println("num value is 2");
break;

case 3 : 
System.out.println("num value is 3");
break;

default :
System.out.println("num value is not initialized");
}
}}